
import java.util.*;

public class StringSort {

    public static void main(String[] args) {

        String[] list = new String[]{"s","q","o","i","p","x","l","k","h","w","e","r","y","u"};

        Arrays.sort(list);

        for (int i = 0; i < list.length; i++) {
            System.out.print(list[i] + " ");
        }

    }


}
