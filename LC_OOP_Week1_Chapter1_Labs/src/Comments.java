
public class Comments {

	public static void main(String[] args) {
		System.out.println("Program comments are nonexecuting statements you add to a file for the purpose of documentation.\n");
		System.out.println("There are 3 ways to add comments in Java Programs, which are demonstrated as follows:\n");
		// This is a Line Comment template.
		System.out.println("1. Line Comment added as:\n// Program comments are nonexecuting statements you add to a file for the purpose of documentation.\n");
		/*This is a
		  Block
		  Comment template.*/
		System.out.println("2. Block Comment added as:\n/* Program comments are nonexecuting \n   statements you add to a file for the \n   purpose of documentation. */\n");
		/**
		 * This is a Javadoc Comment template.
		 **/
		System.out.println("3. Javadoc Comment can be written as follows:\n/**\n * Program comments are nonexecuting statements you add to a file for the purpose of documentation.\n **/");

	}

}